import java.util.*;

public class Loops {
	public static void main(String [] args) {

		//Scanner sc = new Scanner(System.in);

		// for loop

		for (int i = 0; i < 10; i++) {
			System.out.println(i);
		}


		// while loop

		int i = 0;

		while (i < 10) {
			System.out.println(i);

			i++;
		}

		// do while loop

		do {
			System.out.println(i);
			i--;
		} while (i >= 0);


	}
}